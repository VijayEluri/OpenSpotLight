package org.openspotlight.security.authz;

import java.security.GeneralSecurityException;

/**
 * This exception is thrown by the {@link PolicyEnforcement} instances. This exception describes a enforcement problem.
 * 
 * @author porcelli
 */
public class EnforcementException extends GeneralSecurityException {

    /** The Constant serialVersionUID. */
    private static final long serialVersionUID = -4821781765446391854L;

    /**
     * Instantiates a new enforcement exception.
     */
    public EnforcementException() {
        super();
    }

    /**
     * Instantiates a new enforcement exception.
     * 
     * @param message the message
     */
    public EnforcementException(
                                 final String message ) {
        super(message);
    }

    /**
     * Instantiates a new enforcement exception.
     * 
     * @param message the message
     * @param cause the cause
     */
    public EnforcementException(
                                 final String message, final Throwable cause ) {
        super(message, cause);
    }

    /**
     * Instantiates a new enforcement exception.
     * 
     * @param cause the cause
     */
    public EnforcementException(
                                 final Throwable cause ) {
        super(cause);
    }
}
